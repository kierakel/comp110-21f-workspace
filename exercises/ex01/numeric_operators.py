"""A program to practice with numeric operators."""

__author__ = "730350912"

left: int = int(input("Left-hand side: "))
right: int = int(input("Right-hand side: "))
print(str(left) + " ** " + str(right) + " is " + str(left ** right))
print(str(left) + " / " + str(right) + " is " + str(left / right))
print(str(left) + " // " + str(right) + " is " + str(left // right))
print(str(left) + " % " + str(right) + " is " + str(left % right))